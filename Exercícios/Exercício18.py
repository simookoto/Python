import math
an=float(input('digite um angulo:'))
se=math.sin(math.radians(an))
print('o angulo é {} e o seno é {:.2f}' .format(an, se, ))
co=math.cos(math.radians(an))
print('o angulo é {} e o coseno é {:.2f}' .format(an, co))
ta=math.tan(math.radians(an))
print('o angulo é {} e o tangente é {:.2f}' .format(an, ta))